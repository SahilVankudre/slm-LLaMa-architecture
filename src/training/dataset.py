"""
src/training/dataset.py

TinyStories Dataset Loader
Handles loading, tokenizing, and batching TinyStories data for training.

Dataset: roneneldan/TinyStories (HuggingFace)
- ~2.1M synthetic short stories
- Generated by GPT-3.5/GPT-4
- Designed for training small language models
- Simple vocabulary, good for testing

Data pipeline:
  1. Load from HuggingFace (streaming or download)
  2. Tokenize text
  3. Chunk into sequences of max_seq_len
  4. Create batches for training
"""

import torch
from torch.utils.data import Dataset, DataLoader
from datasets import load_dataset
from typing import Optional, Iterator
import random


class TinyStoriesDataset(Dataset):

    def __init__(
        self,
        tokenizer,
        split: str = "train",
        max_seq_len: int = 512,
        max_samples: Optional[int] = None,
        streaming: bool = False
    ):
        self.tokenizer = tokenizer
        self.max_seq_len = max_seq_len
        self.split = split
        
        print(f"Loading TinyStories ({split} split)...")
        
        self.dataset = load_dataset(
            "roneneldan/TinyStories",
            split=split,
            streaming=streaming
        )
        
        if max_samples is not None and not streaming:
            self.dataset = self.dataset.select(range(min(max_samples, len(self.dataset))))
        
        if not streaming:
            self.length = len(self.dataset)
            print(f"Loaded {self.length} samples")
        else:
            self.length = None
            print(f"Streaming mode enabled")
    
    def __len__(self):
        if self.length is None:
            raise ValueError("Length not available in streaming mode")
        return self.length
    
    def __getitem__(self, idx):

        sample = self.dataset[idx]
        text = sample['text']

        tokens = self.tokenizer.encode(text, add_special_tokens=False)
        
        tokens.append(self.tokenizer.eot_token_id)
        
        if len(tokens) > self.max_seq_len + 1:
            tokens = tokens[:self.max_seq_len + 1]
        
        tokens = torch.tensor(tokens, dtype=torch.long)

        input_ids = tokens[:-1]
        labels = tokens[1:]
        
        return {
            'input_ids': input_ids,
            'labels': labels
        }


class TinyStoriesDataLoader:

    def __init__(
        self,
        dataset: TinyStoriesDataset,
        batch_size: int = 32,
        shuffle: bool = True,
        num_workers: int = 0
    ):
        self.dataset = dataset
        self.batch_size = batch_size
        self.shuffle = shuffle
        
        self.dataloader = DataLoader(
            dataset,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            collate_fn=self._collate_fn,
            pin_memory=True 
        )
    
    def _collate_fn(self, batch):

        max_len = max(len(sample['input_ids']) for sample in batch)
        
        input_ids_batch = []
        labels_batch = []
        
        for sample in batch:
            input_ids = sample['input_ids']
            labels = sample['labels']
            
            pad_len = max_len - len(input_ids)
            
            if pad_len > 0:
                input_ids = torch.cat([
                    input_ids,
                    torch.zeros(pad_len, dtype=torch.long)
                ])
                labels = torch.cat([
                    labels,
                    torch.full((pad_len,), -100, dtype=torch.long)
                ])
            
            input_ids_batch.append(input_ids)
            labels_batch.append(labels)
        
        input_ids_batch = torch.stack(input_ids_batch)
        labels_batch = torch.stack(labels_batch)
        
        return {
            'input_ids': input_ids_batch,
            'labels': labels_batch
        }
    
    def __iter__(self):
        return iter(self.dataloader)
    
    def __len__(self):
        return len(self.dataloader)

class SimpleTextDataset(Dataset):

    def __init__(self, tokenizer, texts, max_seq_len=512):
        self.tokenizer = tokenizer
        self.max_seq_len = max_seq_len
        self.texts = texts
    
    def __len__(self):
        return len(self.texts)
    
    def __getitem__(self, idx):
        text = self.texts[idx]
        tokens = self.tokenizer.encode(text, add_special_tokens=False)
        tokens.append(self.tokenizer.eot_token_id)
        
        if len(tokens) > self.max_seq_len + 1:
            tokens = tokens[:self.max_seq_len + 1]
        
        tokens = torch.tensor(tokens, dtype=torch.long)
        
        return {
            'input_ids': tokens[:-1],
            'labels': tokens[1:]
        }

